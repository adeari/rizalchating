              <blockquote>
                  <p class="style11">PHP (PHP: Hypertext  Preprocessor) adalah sebuah bahasa pemrograman yang awalnya didesain untuk  membuat halaman web dinamis. PHP digunakan utamanya dalam pemrograman skrip  sisi server, tetapi dapat digunakan dari sebuah antarmuka command line atau  dalam aplikasi grafis standalone. Implementasi utama dibuat PHP Group dan  dirilis di bawah lisensi PHP. Implementasi ini melayani untuk mendefinisikan sebuah  standard de facto untuk PHP, karena tidak ada spesifikasi formal.</p>
                  <p class="style11">                    PHP ditulis sebagai  sebuah set binari Common Gateway Interface (CGI) dalam bahasa pemrograman C  oleh Rasmus Lerdorf di tahun 1994, untuk menggantikan sebuah set skrip Perl  yang dia gunakan untuk mengelola website pribadinya.</p>
                  <p class="style11">                    PHP adalah bahasa skrip  yang digunakan secara luas khususnya untuk pengembangan web dan dapat  disertakan dalam HTML. PHP secara umum berjalan di sebuah web server, dimana  web server mengambil kode PHP sebagai input dan membuat halaman web sebagai  output. Tetapi, PHP juga dapat digunakan untuk skrip command line dan aplikasi  GUI sisi client. PHP dapat dijalankan di hampir semua web server dan hampir  semua sistem operasi dan platform secara gratis. PHP Group juga menyediakan  source code lengkap bagi user untuk membangun, mengubah, dan menambah  penggunaan mereka sendiri. PHP umumnya bertindak sebagai filter. Program PHP  mengambil input dari sebuah file atau stream berisi teks dan instuksi khusus  PHP dan output stream yang lain dari data untuk ditampilkan.</p>
                  <p class="style11">                    Awalnya didesain untuk  membuat halaman web dinamis, PHP memiliki fokus dalam skrip sisi server. Ketika  menjalankan parser PHP dengan sebuah web server dan web browser, model PHP  dapat dibandingkan dengan bahasa skrip sisi server seperti ASP .NET milik Microsoft,  JavaServer Pages milik Sun Microsystems, mod_perl dan Ruby on Rails sebagaiman  mereka semua menyediakan isi dinamis untuk client dari sebuah web server. <br>
                    Arsitektur LAMP menjadi  populer dalam industri web sebagai jalan untuk membuat aplikasi web yang aman,  reliabel dan murah. PHP dapat digunakan dengan sejumlah RDBMS, 
                    berjalan di sebagian besar web  server dan tersedia untuk berbagai sistem operasi berbeda. Fleksibilitas ini  berarti PHP memiliki sebuah basis instalasi luas di internet, lebih dari 19  juta domain internet saat ini dihosting pada server yang menggunakan PHP. Contoh  aplikasi PHP sisi server yang populer adalah phpBB, WordPress, dan MediaWiki.<br>
                    PHP juga  menyediakan sebuah antarmuka command line untuk mengembangkan shell dan  aplikasi desktop, daemon, parsing log, atau perintah administrasi sistem yang  lain. PHP meningkat digunakan dalam command line untuk perintah yang secara  tradisional adalah domain Perl, Python, awk, atau skrip shell.<br>
                    PHP  menyediakan penyertaan librari GUI seperti GTK+, Qt dan librari mode teks  seperti ncurses untuk memfasilitasi pengembangan aplikasi GUI cross platform  yang lebih luas. PHP bisa didapatkan di <a href="http://php.net/">http://php.net/</a>.&nbsp;&nbsp;</p>
