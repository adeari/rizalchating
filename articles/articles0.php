              <td colspan="3"><div align="justify">
                <blockquote>
                  <p class="style11">Ajax bukan sebuah bahasa  pemrograman baru, namun sebuah teknik untuk menciptakan aplikasi web yang lebih  baik, lebih cepat, dan lebih interaktif. Ajax berbasiskan standard web berikut:</p>
                  <ul class="style11">
                    <li>JavaScript</li>
                    <li>HTML</li>
                    <li>CSS</li>
                  </ul>
                  <p class="style11">Semua standard web yang  digunakan dalam Ajax telah didefinisikan dengan baik dan didukung oleh sebagian  besar browser.                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;</p>
                  <p><span class="style11">Dengan Ajax,  JavaScript dapat berkomunikasi secara langsung dengan server, menggunakan objek  JavaScript yaitu XMLHttpRequest. Dengan objek ini, JavaScript dapat bertukar  data dengan web server, tanpa meload ulang halaman. </span>&nbsp;</p>
                  <p align="center"><img src="../images/untitled.JPG" width="578" height="422"><br>
                    <span class="style11">Perbandingan antara  website biasa dengan website yang menggunakan Ajax</span></p>
                  <p class="style11">Dalam pengkodean JavaScript tradisional, jika  ingin 
                  mendapatkan informasi  dari sebuah database atau sebuah file di server, atau mengirim informasi user ke  sebuah server, harus dibuat sebuah form HTML dan GET atau POST data ke server.  User harus menekan tombol &rdquo;Submit&rdquo; untuk mengirim/menerima informasi, menunggu  server untuk merespon, kemudian sebuah halaman baru akan diload dengan  hasilnya. Karena server mengembalikan sebuah halaman baru tiap kali user  mengirim input, aplikasi web tradisional dapat berjalan pelan dan cenderung  kurang user-friendly.</p>
                  <p class="style11"> Dengan Ajax, JavaScript berkomunikasi langsung dengan server,  melalui objek XMLHttpRequest. Dengan sebuah request HTTP, sebuah halaman web  dapat membuat request dan menerima respon dari web server tanpa meload ulang  halaman. User akan berada pada halaman yang sama, dan tidak mengetahui skrip  meminta halaman atau mengirim data ke 
                  server. Objek  XMLHttpRequest didukung di Internet Explorer 5.0 ke atas, Safari 1.2, Mozilla  1.0 / Firefox, Opera 8 ke atas, dan Netscape 7.</p>
                  <p class="style11"> Kunci dari Ajax adalah objek XMLHttpRequest. Browser yang  berbeda menggunakan method yang berbeda untuk membuat objek XMLHttpRequest. Internet  Explorer menggunakan sebuah ActiveXObject, sementara browser yang lain  menggunakan objek JavaScript built-in yang disebut XMLHttpRequest. </p>
