            <td colspan="3"><div align="justify">
              <p align="center"><img src="../images/mysql.gif" width="110" height="58"></p>
              <blockquote>
                <p class="style11"> MySQL adalah database management system  (DBMS) SQL multiuser. MySQL dimiliki dan disponsori oleh perusahaan MySQL AB  yang memegang hak cipta sebagian besar basis kode. MySQL AB membangun dan mengelola sistem,  menjual kontrak layanan dan dukungan, termasuk salinan MySQL berlisensi, dan memperkerjakan  orang di seluruh dunia yang berkolaborasi melalui internet. MySQL AB dibangun oleh David Axmark, Allan Larsson,  dan Michael Widenius. </p>
                <p class="style11">                  Librari untuk mengakses database MySQL  tersedia dalam semua bahasa pemrograman besar dengan API khusus. Sebagai  tambahan, sebuah antarmuka ODBC yang disebut MyODBC memungkinkan bahasa  pemrograman tambahan yang mendukung antarmuka ODBC untuk berkomunikasi dengan  sebuah database MySQL, seperti ASP atau Coldfusion. Server MySQL dan librari resmi  sebagian besar diimplementasikan dari ANSI C.</p>
                <p class="style11">                  MySQL  populer untuk aplikasi web dan bertindak sebagai komponen database dalam  platform LAMP. Popularitasnya sebagai aplikasi web mendekati popularitas PHP,  yang biasa digabungkan dengan MySQL. PHP dan MySQL adalah komponen penting  untuk menjalankan platform blog WordPress. Wikipedia berjalan di software  MediaWiki yang juga menggunakan PHP dan database MySQL.</p>
                <p class="style11">                  Untuk  mengelola database MySQL dapat digunakan tool command-line (perintah: mysql dan  mysqladmin). Juga dapat digunakan tool administrasi GUI MySQL Administrator dan  MySQL Query Browser. Kedua tool GUI dimasukkan dalam MySQL GUITools. Selain  itu, ada beberapa tool komersial dan non-komersial yang tersedia. PhpMyAdmin  adalah sebuah software gratis dengan antarmuka administrasi berbasis web dan  diimplementasikan dalam PHP.</p>
                <p class="style11">                  MySQL  berjalan di berbagai platform, diantaranya: AIX, FreeBSD, GNU/Linux, Mac OS X,  NetBSD, Novell NetWare, Open BSD, Solaris, SunOS, Windows 95, Windows 98, Windows  ME, Windows NT, Windows 2000, Windows XP, dan Windows Vista. Fitur berikut  diimplemetasikan dalam MySQL:</p>
              </blockquote>
              <ul class="style11">
                <li>Banyak mesin penyimpanan, memungkinkan untuk memilih satu yang paling efektif untuk  tiap tabel dalam aplikasi, diantaranya: mesin penyimpanan yang dikembangkan  oleh MySQL AB (MyISAM, Falcon, Merge,  Federated, Archive, CSV), mesin penyimpanan yang dikembangkan partner MySQL AB  (InnoDB, solidDB, NitroEDB, BrightHouse), mesin penyimpanan yang dikembangkan  oleh komunitas, dan mesin penyimpanan custom.</li>
                <li>Melakukan commit pengelompokan, mengumpulkan berbagai transaksi dari berbagai koneksi bersama untuk meningkatkan jumlah commit tiap detik.<br>
                 Terdapat  3 jenis kompilasi MySQL Server:</li>
                <li>Binari MySQL-Standard, direkomendasikan untuk sebagian besar user, termasuk mesin penyimpan InnoDB. </li>
                <li>Binari MySQL-Max termasuk fitur tambahan yang mungkin belum diuji  secara ekstensif atau tidak digunakan untuk penggunaan umum.</li>
                <li>&nbsp;Binari MySQL-Debug  dikompilasi dengan informasi debug tambahan dan tidak  diperuntukkan untuk produksi karena terdapat kode debug yang mungkin  menyebabkan berkurangnya kinerja.</li>
              </ul>
              <blockquote>
                <p class="style11">                Sejak MySQL 5.1, MySQL AB  berhenti menyediakan tiga paket berbeda. Hanya ada satu paket server MySQL yang  termasuk sebuah binari mysqld dengan semua fungsionalitas dan mesin penyimpan  diaktifkan. Binari server dengan informasi debug tambahan juga dimasukkan dalam  paket standard. MySQL ditulis dalam bahasa C dan C++. MySQL dapat diperoleh di <a href="http://www.mysql.com/">http://www.mysql.com/</a>.</p>
              </blockquote>
