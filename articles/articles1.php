     <tr>
       <td colspan="3"><div align="justify">
         <blockquote>
           <p align="center"><img src="../images/apache.jpg" width="115" height="48"></p>
           <p align="justify" class="style11">Apache HTTP Server, biasa disebut Apache, adalah  sebuah 
             web server yang berperan penting dalam perkembangan  World 
             Wide Web. Apache adalah alternatif pertama dari web  server milik 
             Netscape (yang saat ini dikenal sebagai Sun Java  System Web 
             Server), dan lambat laun menjadi saingan bagi web  server lain 
             berbasis Unix dalam hal  fungsionalitas dan kinerja.</p>
           <p class="style11"> Apache  dikembangkan dan dikelola oleh komunitas pengembang yang terbuka di bawah  Apache Software Foundation. 
             Aplikasi ini tersedia untuk  berbagai sistem operasi, termasuk Unix, 
             FreeBSD, Linux, Solaris, Novell NetWare, Mac OS X, dan 
             Microsoft Windows. Dirilis di bawah lisensi Apache,  Apache 
             dikategorikan sebagai Software  gratis dan open source.Sejak April 
             1996, Apache menjadi HTTP server paling populer di  World Wide 
             Web. Pada bulan April 2008,  Apache melayani 50,42% dari semua 
             website.</p>
           <p class="style11"> Versi pertama dari web server  Apache dibuat oleh Robert 
             McCool, yang dipengaruhi oleh web  server NCSA yang dikenal sebagai NCSA HTTPd. Ketika Robert meninggalkan NCSA di  pertengahan 1994, pengembangan httpd diperbanyak, meninggalkan bermacam patch  untuk perbaikan beredar melalui e-mail. </p>
           <p class="style11">Apache mendukung berbagai fitur,  banyak yang 
             diimplementasikan sebagai modul  terkompilasi yang memperluas 
             fungsi utamanya. Hal ini dapat  berkisar dari dukungan bahasa 
             pemrograman sisi server hingga  skema autentikasi. Beberapa &nbsp;antarmuka  bahasa biasa mendukung mod_perl, mod_python, Tcl, dan PHP. Modul autentikasi  yang populer termasuk mod_access, mod_auth, dan mod_digest. Contoh fitur lain  termasuk dukungan SSL dan TLS (mod_ssl), sebuah modul proxy, sebuah penulis  ulang URL (mode_rewrite), mengubah file log (mod_log_config), dan dukungan  filtering (mod_include dan mod_ext_filter).</p>
           <p class="style11">             Metode  kompresi populer di Apache termasuk modul tambahan eksternal, mod_gzip,  diimplementasikan untuk membantu mengurangi ukuran (berat) halaman web yang  dilayani oleh HTTP. Log Apache dapat dianalisa melalui sebuah browser web  menggunakan skrip gratis seperti AWStats/W3Perl atau Visitors. Hosting  virtual memungkinkan satu instalasi Apache melayani 
             berbagai website yang berbeda.  Sebagai contoh, satu mesin, dengan satu instalasi Apache dapat secara simultan  melayani www.contoh.com, www.tes.com, tes47.tes-server.tes.com, dan lain-lain. Apache  dilengkapi pesan kesalahan yang dapat dikonfigurasi, database autentikasi  berbasis DBMS, dan negosiasi isi. Ini juga didukung oleh sejumlah antarmuka  grafis (GUI) yang memungkinkan konfigurasi server yang lebih mudah.</p>
           <p class="style11">             Apache biasanya digunakan untuk melayani halaman web  statis dan dinamis di World Wide Web. Banyak aplikasi web yang didesain  berdasarkan fitur yang disediakan Apache. Apache adalah komponen web server  dari LAMP, disamping MySQL, dan bahasa pemrograman PHP/Perl/Python. Apache  didistribusikan sebagai bagian dari berbagai paket software termasuk database  Oracle. Mac OS X mengintegrasikan Apache sebagai web server built-in dan  sebagai pendukung untuk aplikasi server WebObjects. Ini juga didukung oleh  Borland dalam Kylix dan Delphi. Apache dimasukkan dalam Novell NetWare 6.5  sebagai default web server. </p>
           <p class="style11">             Apache digunakan untuk  berbagai perintah dimana isi perlu tersedia secara aman dan dapat dipercaya.  Satu contoh adalah berbagi file dari komputer pribadi melalui internet. Seorang  user yang menginstal Apache di komputernya dapat menaruh file semaunya di root  dokumen Apache yang kemudian dapat dibagi. Programer membangun aplikasi web  biasanya menggunakan sebuah Apache yang terinstal secara lokal untuk melihat  melihat sementara dan menguji kode saat dikembangkan.</p>
           <p class="style11">             Microsoft Internet  Information Services (IIS) adalah kompetitor utama Apache, diikuti Sun Java  System Web Server milik Sun Microsystems, dan sebuah host dari aplikasi lain  seperti Zeus Web Server. Sejumlah website besar di dunia menggunakan Apache.  Mesin pencari milik Google berbasiskan Apache yang telah dimodifikasi yang  disebut Google Web Server. Proyek  Wikimedia, termasuk Wikipedia, juga menggunakan Apache. Apache bisa diperoleh  di             <a href="http://httpd.apache.org/">http://httpd.apache.org/</a></p>
